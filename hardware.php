<h2>1) Le processeur </h2>
<strong>Type : nécessaire </strong>
<img src="Style/img/hardware/processeur.jpg" alt="processeur">
<p>Le processeur ou en anglais CPU (central processing traitement) d'un ordinateur permet d'excuter les instruction des programmes
autrement dit il s'agit d'une puissante calculatrice,chaque programme nécessite une certaine puissance de calcul. Il s'agit donc 
de choisir judicieusement son processeur en fonction de son utilisation. </p>
<p> On va retenir 3 critéres important : l'architecture, la fréquence, le nombre de coeur/thread <br> 
<strong> La fréquence :</strong> la fréquence d'un processeur exprimer en Giga-hertz (Gh) est le nombre de calcul que le processeur peut effectuer en une seconde,  <br>
<strong> Nombre de coeur </strong> La fréquence nest plus lélément qui reflète toute la puissance dun processeur. Depuis maintenant de nombreuses années, les processeurs de type Dual Core sont très courants. Ils peuvent réaliser deux tâches conséquentes en même temps ou une tâche lourde tout en préservant la « réactivité » du système. <br>
Les processeurs Quad Core offrent une puissance encore plus importante qui est exploitée par de plus en plus de logiciels « lourds » ainsi que par un nombre grandissant de jeux vidéo. Les programmes dédition vidéo, de traitement/composition audio, de retouche photos professionnel, de transcodage vidéo (conversion de formats), de (dé)compression de fichiers, de chiffrement et de rendu 3D exploitent souvent pleinement ce type de processeurs.
Intel ne sest pas arrêté en si bon chemin et propose même des processeurs Hexa Core ! Cette énorme puissance de calcul booste encore les applications qui tirent déjà parti des processeurs à quatre curs

</p>
