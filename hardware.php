il n'ai pas rare de voir de nos jour des disque de 1To. Le disque dur posséde un sytéme de mémoire mécanique (donc potentiellement bruyant)
une broche va saisir les bits d'information sur un disque en ... la vitesse d'un HDD se mesure par le nombre de tours par minute : 4500 ou 7200 <br>
Il est important d'avoir une bonne vitesse de transfert pour votre systéme de stockage. C'est ce qui determinera la rapidité avec laquel
se lanceront vos programme (ce qui inclue le démarrage de votre pc) </p>
