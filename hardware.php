<h2>3) L'alimentation </h2>
<strong>Type : nécessaire </strong>
<img src="Style/img/hardware/alimentation.gif" alt="processeur">
<p> L'alimentation d'un ordinateur, L’alimentation est chargée de convertir la tension électrique du secteur en différentes 
tensions continues TBT(tres basse tension), compatibles avec les circuits électroniques de l’ordinateur.Les caractéristique
qui doivent vous faire choisir votre alimentation sont : <br>
<strong>La puissance : </strong> en fonction de vos composants, il vous faut une certaine puissance exprimer en watts (W).
Une alimentation insuffisante et votre pc ne s'allumera pas. Et il est inutile de prendre une aliemntation de 1000 watts
pour regarder ces mails Grosso modo : <br>
	430 Watts : Utilisés pour les configurations Bureautique, multimédia ou HTPC.<br>
    550 Watts : Cette puissance est suffisante dans 80% des configurations de jeux.<br>
    620-650 Watts : Dans le cas d'une carte graphique haut de gamme Nvidia ou à partir du milieu de gamme AMD (elles consomment plus).<br>
    750-850 Watts : Dans le cas d'un SLI/Crossfire de cartes graphiques haut de gamme..<br>
    Pour les néophytes, cela correspond à brancher 2 cartes graphiques et à les relier entre-elles.
    1000 Watts : Dans le cas d'un Quad-SLI/QuadFire (4 cartes graphiques haut de gamme) + overclocking + processeur i7/Phenom II x6.<br>
Pour plus de précision je vous proposer ce site qui permet de calculer l'alimenation idéale 
<a href="https://www.bequiet.com/fr/psucalculator"> Calculator</a> <br>

<strong>Le certifificat : </strong> le rendement d'une alimentation (exprimer en %) se determine avec un certificat, plus le rendement est 
elever plus la consommation éléctrique est moindre. <br>  
<img src="Style/img/hardware/certificat.png" alt="certificat">

<strong> Modulaire ou non :</strong> C'est un détail qui a son importance, en fait il y a trois possibilité : <em>modulaire</em>,
<em>semi modulaire</em>,<em>non modulaire</em>. Une alimentation modulaire signifie qu'il vous est possible de retirer les connectiques
qui ne vous importe pas. Ainsi votre montage sera plus propre et plus aisé en evitant le syndrome du plat de spagethie : 
<img src="Style/img/hardware/mauvais_montage.jpg" alt="montage"> Voilas un beau mélange de cable <br>

Ceci n'est pas seulement esthetique; un montage bien réaliser permettera a l'air de mieux circuler dans votre tours donc d'avoir un meilleurs
refroidissement. <br>

<strong> Les connectiques : </strong> Voicis quelque chose de tres important, assurer vous que chacun de vos composant ai les bon port pour pouvoir
être alimenter, vérifier le type de connectique, le nombre de broche etc <br>    
</p>
