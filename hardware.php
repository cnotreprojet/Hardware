<h2>5)La mémoire : </h2> 
<strong>Type : nécessaire</strong>
<p>La je n'ai pas mit un composant en particulier car vous avez plusieur possiblité : <br>
<strong> Un disque dur HDD</strong> : le prix des disques dur a beaucoup baisser depuis années avec toujours plus importante, 
